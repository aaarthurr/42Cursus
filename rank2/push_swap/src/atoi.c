#include "../include/push_swap.h"

long int	ft_atoi(char *s)
{
	int			sign;
	long int	r;

	r = 0;
	sign = 1;
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	if (!(*s >= 48 && *s <= 57))
		return (99999999999);
	while (*s >= '0' && *s <= '9')
	{
		r = r * 10 + *s - '0';
		s++;
	}
	return (sign * r);
}
